/**
  * @brief This function handles USART6 global interrupt.
  */
void USART6_IRQHandler(void)
{
  /* USER CODE BEGIN USART6_IRQn 0 */

	RX_data[0] = USART6->RDR;
	TX_buf[TX_buf_Tail++] = RX_data[0];

	if(RX_data[0]=='\r')
		{
			TX_str_leng = TX_buf_Tail-1;

			memset(TX_str, '\0', 10);
			memcpy(TX_str, TX_buf, TX_str_leng);
			memset(TX_buf, '\0', 10);

			HAL_UART_Transmit(&huart6, LFCR, 2, 100);

			TX_buf_Tail=0;
			Motor_flag=1;
		}

	if(RX_data[0]=='w')
		{
			Motor_flag=2;
		}
	if(RX_data[0]=='s')
		{
			Motor_flag=3;
		}
	if(RX_data[0]=='a')
		{
			Motor_flag=4;
		}
	if(RX_data[0]=='d')
		{
			Motor_flag=5;
		}

	HAL_UART_Transmit(&huart6, RX_data, 1, 100);

  /* USER CODE END USART6_IRQn 0 */
  HAL_UART_IRQHandler(&huart6);

  /* USER CODE BEGIN USART6_IRQn 1 */

  /* USER CODE END USART6_IRQn 1 */
}

